#include "libft.h"

void	ft_free(char ***str)
{
	size_t i;

	i = 0;
	if (str != NULL)
	{
		while (*(*str + i) != 0)
		{
			free(*(*str + i));
			i++;
		}
		free(*str);
		*str = NULL;
	}
}

